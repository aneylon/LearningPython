
# variables are created through assignment
myVar = 'Some Value'

# variables are dynamically typed
myVar = 123

# variables can be cast to a specific type
myVar = float(3.0)

# the type of a variable can be determined
print(type(myVar))

# variables are case sensitive
MyVar = 'Is a different variable from myVar'

print(myVar)
print(MyVar)

# variables can be deleted
del myVar
